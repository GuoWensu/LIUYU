#include<stdio.h>
 
int f(int n,int m)//n代表元素个数，m代表子集
{
    if(m==1||n==m)//情况一：若m==1，则f(n,m)=1。情况二：若n==m，则f(n,m)=1。
        return 1;
    else
        return f(n-1,m-1)+f(n-1,m)*m;//若不符合以上两种情况，则有以下情况：
	//情况一：在n-1个元素划分成的m-1个集合里添加一个由一个元素形成的独立的集合，则有f(n-1,m-1)种方法；
	//情况二：在n-1个元素划分成的m个集合里面添加一个新的元素，则有m*f(n-1,m)种方法。
}
 
int main() 
{
    int n;
    while(scanf("%d",&n)==1&&n>=1)
    {
        int i;
        int sum=0;
        for(i=1;i<=n;i++)
        {
            sum+=f(n,i); //此程序的时间复杂度为O(n)
        }
        printf("%d\n",sum);
    }
    return 0;//返回主函数main（）的值
}
